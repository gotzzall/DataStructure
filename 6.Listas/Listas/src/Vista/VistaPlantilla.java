    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        if (!click) {
            txtDato.setEnabled(!click);
            click = true;
        } else {
            if (!txtDato.getText().equals("")) {
                try {
                    double dato = Double.parseDouble(txtDato.getText());
                    txtResultado.setText(opb.busquedaSecuencial(dato));
                    txtDato.setEnabled(!click);
                    click = false;
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, "Ingrese valores numéricos");
                }
            } else {
                JOptionPane.showMessageDialog(null, "El campo no pueden estar vacios");
            }
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

