    private void btnInsertarDondesesaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarDondesesaActionPerformed
        if (!click) {
            txtDato.setEnabled(!click);
            txtAnterior.setEnabled(!click);
            click = true;
        } else {
            if (!txtDato.getText().equals("") || !txtAnterior.getText().equals("")) {
                try {
                    double anterior = Double.parseDouble(txtAnterior.getText());
                    double dato = Double.parseDouble(txtDato.getText());
                    opb.insertearDondeSea(anterior, dato);
                    txtDato.setEnabled(!click);
                    txtAnterior.setEnabled(!click);
                    click = false;
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, "Ingrese valores numéricos");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Los campos no pueden estar vacios");
            }
        }
    }//GEN-LAST:event_btnInsertarDondesesaActionPerformed

