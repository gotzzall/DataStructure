    private void btnInsertarFrenteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarFrenteActionPerformed
        if (!click) {
            txtDato.setEnabled(!click);
            click = true;
        } else {

            if (!txtDato.getText().equals("")) {
                try {
                    double dato = Double.parseDouble(txtDato.getText());
                    opb.insertarAlFrente(dato);
                    if (flag == false) {
                        flag = true;
                        actualizarBotones(flag);
                    }
                    txtDato.setEnabled(!click);
                    click = false;
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, "Ingrese valores numéricos");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Los campos no pueden estar vacios");
            }
        }
    }//GEN-LAST:event_btnInsertarFrenteActionPerformed

